package javatutorials;
import java.util.*;
public class num1loop {
    public static void main(String arr[]){
         try (Scanner sc = new Scanner(System.in)){
            int n = sc.nextInt();
            int sum=0;
            for (int i=1; i<=n;i++){
                sum=sum+i;
                System.out.println(sum);
            }
         }
       
    }
    }
    

